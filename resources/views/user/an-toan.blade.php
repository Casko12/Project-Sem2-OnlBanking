                        <p style="margin-bottom: 10pt; text-align: justify;"><span style="letter-spacing: -0.1pt;">“Trong định hướng phát triển của ngân hàng, Modern Bank  luôn chú trọng xây dựng hệ thống an toàn thông tin phù hợp và tuân theo các tiêu chuẩn quốc tế. Chứng chỉ ISO/IEC 27001: 2013 chỉ là bước khởi đầu cho chặng đường dài phía trước khi chúng tôi phải tiếp tục duy trì và cải tiến liên tục hệ thống nhằm không ngừng giảm thiểu rủi ro về an toàn thông tin”, vị đại diện này chia sẻ. </span></p>
